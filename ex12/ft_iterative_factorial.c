int		ft_iterative_factorial(int nb)
{
	int i;
	int fact;

	i = 1;
	fact = 0;
	if (nb <= 12 && nb >= 0)
	{
		fact++;
		while (i <= nb)
		{
			fact *= i;
			i++;
		}
	}
	return (fact);
}
